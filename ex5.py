n1 = float(input("Digite o primeiro número:"))
n2 = float(input("Digite o segundo número:"))
adicao = n1+n2
print(f"O resultado da adição e: {adicao:.3f}")

subtracao = n1-n2
print(f"O resultado da subtração é: {subtracao:.3f}")

multiplicacao = n1*n2
print(f"O resultado da multiplicação e: {multiplicacao:.3f}")

divisao = n1/n2
print(f"O resultado da divisao e: {divisao:.3f}")